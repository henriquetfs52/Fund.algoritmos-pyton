x = int(input("Digite um número"))
if x > 0 and x < 8:
    if x == 1:
        print("Seu dia da semana é domigo")
    if x == 2:
        print("Seu dia da semana é segunda")
    if x == 3:
        print("Seu dia da semana é terça")
    if x == 4:
        print("Seu dia da semana é quarta")
    if x == 5:
        print("Seu dia da semana é quinta")
    if x == 6:
        print("Seu dia da semana é sexta")
    if x == 7:
        print("Seu dia da semana é sábado")
else:
    print("seu dia da semana não existe")